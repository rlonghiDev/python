print("IMPORTANTE: Para o cálculo correto da área as medidas a serem inseridas precisam estar na mesma unidade de medida\n")

#Coleta de Dados 
Base = float(input("Digite a medida da Base\n"))
Altura = float(input("Digite a medida da Altura\n"))

#Processamento
Area = Base * Altura

#Saída de Dados
print("\nA área do retângulo é: ", Area)